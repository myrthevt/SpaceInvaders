package spaceinvaders;

/**
 *
 * @author vantichelen
 */
public class Steen {
    private int x;
    private int y, dy;

    public Steen(int x, int y) {
        this.x = x;
        this.y = y;
    }

    /**
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * @param x the x to set
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * @return the y
     */
    public int getY() {
        return y;
    }

    /**
     * @param y the y to set
     */
    public void setY(int y) {
        this.y = y;
    }
    
   public void BeweegNaarOnder(){
       y = y + 15;
   }
   
   public void tick (){
       y = y + dy;
   }
    

    
}
